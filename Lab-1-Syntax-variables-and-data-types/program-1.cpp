/**
 * Write a Program to calculate and display the volume of a CUBE having its height (h=10cm), width(w=12cm) and depth (8cm)
 */
#include <iostream>
using namespace std;

int main(){
    int h=10;
    int w=12;
    int depth =8;
    int vol = h * w* depth; 
    cout<< "volume is " << vol <<endl; 
    return 0;
}